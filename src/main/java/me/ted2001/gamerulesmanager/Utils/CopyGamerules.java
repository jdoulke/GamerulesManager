package me.ted2001.gamerulesmanager.Utils;

public class CopyGamerules {

    String gameRule;
    String value;

    public String getGameRule() {
        return gameRule;
    }

    public String getValue() {
        return value;
    }



    public CopyGamerules(String gameRule, String value) {
        this.gameRule = gameRule;
        this.value = value;
    }
}
